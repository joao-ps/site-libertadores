    let positionTable1 = pos1; 
    let positionTable2 = pos2; 
    let positionTable3 = pos3; 
    let positionTable4 = pos4; 
    let position = grupos.sort((a,b) => {
        if(a.p > b.p ){
            return 1
        }
        if(a.p == b.p ){
            if(a.sg > 0){
                if(a.sg > b.sg){
                    return 1
                }
            }
            if(a.sg < 0){
                if(a.sg > b.sg){
                    return 1
                }
            }
        }else{
            return -1
        }
    });

    tdTime[positionTable1].innerText = position[3].time;
